    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

    int fila = jTable1.getSelectedRow();

    if(fila == -1){
        JOptionPane.showMessageDialog(this,"Seleccione un paquete de la tabla");
        return;
    }

    String codigo = txtCodigo.getText();
    String nombre = txtNombre.getText();
    String descripcion = txtDescripcion.getText();
    int dias = Integer.parseInt(txtDías.getText());
    int noches = Integer.parseInt(txtNoches.getText());
    String tipo = txtTipo.getText();
    double precio = Double.parseDouble(txtPrecio.getText());
    String salida = txtFechaSalida.getText();
    String retorno = txtFechaRetorno.getText();
    int cupo = Integer.parseInt(txtCopoMaximo.getText());
    String estado = txtEstado.getText();

    if(gestionPaquete.modificarPaquete(codigo,nombre,descripcion,dias,noches,tipo,precio,salida,retorno,cupo,estado)){

        modelo.setValueAt(codigo,fila,0);
        modelo.setValueAt(nombre,fila,1);
        modelo.setValueAt(tipo,fila,2);
        modelo.setValueAt(precio,fila,3);
        modelo.setValueAt(estado,fila,4);

        JOptionPane.showMessageDialog(this,"Paquete modificado correctamente");

    }else{

        JOptionPane.showMessageDialog(this,"No se encontró el paquete");

    }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

    int fila = jTable1.getSelectedRow();

    if(fila == -1){
        JOptionPane.showMessageDialog(this,"Seleccione un paquete de la tabla");
        return;
    }

    String codigo = txtCodigo.getText();

    if(gestionPaquete.eliminarPaqueteGUI(codigo)){

        modelo.removeRow(fila);

        JOptionPane.showMessageDialog(this,"Paquete eliminado correctamente");

        txtCodigo.setText("");
        txtNombre.setText("");
        txtDescripcion.setText("");
        txtDías.setText("");
        txtNoches.setText("");
        txtTipo.setText("");
        txtPrecio.setText("");
        txtFechaSalida.setText("");
        txtFechaRetorno.setText("");
        txtCopoMaximo.setText("");
        txtEstado.setText("");

    }else{

        JOptionPane.showMessageDialog(this,"No se encontró el paquete");

    }
    }//GEN-LAST:event_jButton3ActionPerformed

