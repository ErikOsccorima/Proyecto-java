    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
String codigo=txtCodigo.getText();
String nombre=txtNombre.getText();
String descripcion=txtDescripcion.getText();
int dias=Integer.parseInt(txtDías.getText());
int noches=Integer.parseInt(txtNoches.getText());
String tipo=txtTipo.getText();
double precio=Double.parseDouble(txtPrecio.getText());
String salida=txtFechaSalida.getText();
String retorno=txtFechaRetorno.getText();
int cupo=Integer.parseInt(txtCopoMaximo.getText());
String estado=txtEstado.getText();

PaqueteTuristico paquete=new PaqueteTuristico(codigo,nombre,descripcion,dias,noches,tipo,precio,salida,retorno,cupo,estado);

if(gestionPaquete.agregarPaquete(paquete)){
    modelo.addRow(new Object[]{paquete.getCodigo(),paquete.getNombre(),paquete.getTipo(),paquete.getPrecioPersona(),paquete.getEstado()});
    javax.swing.JOptionPane.showMessageDialog(this,"Paquete registrado correctamente");
}else{
    javax.swing.JOptionPane.showMessageDialog(this,"No hay espacio para más paquetes");
}
    }//GEN-LAST:event_btnRegistrarActionPerformed

